    ("字体.签发人楷体3号", ["签发人", "楷体"]),
    ("字体.抄送印发仿宋4号", ["抄送机关", "四号(14pt)"]),
    ("字体.页码宋体4号半角", ["宋体", "四号(14pt)", "半角"]),
    ("字体.颜色红黑", ["黑色", "红色"]),
    ("版头.红头居中35mm", ["居中", "35mm"]),
    ("版头.发文字号六角括号虚位", ["发文字号", "六角括号", "虚位"]),
    ("版头.红色分隔线等宽", ["红色分隔线", "FF0000"]),
    ("版头.份号密级紧急签发人依情形", ["H4", "不适用"]),
    ("主体.标题下空二行小标宋居中", ["B1", "下空二行"]),
    ("主体.主送机关左空顶格全角冒号", ["主送机关"]),
    ("主体.正文首行左空二字首页显正文", ["首行左空二字", "firstLineChars=2"]),
    ("主体.结构层次序数一（）1（1）", ["一、→（一）→1.→（1）"]),
    ("主体.附件说明左空二字无标点", ["附件：1.", "无标点"]),
    ("主体.署名右空二字日期右空四字阿拉伯", ["右空二字", "右空四字"]),
