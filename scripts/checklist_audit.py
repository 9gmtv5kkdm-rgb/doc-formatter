    ("主体.附件另面黑体顶格第三行", ["另面", "黑体顶格", "第三行"]),
    ("版记.抄送", ["抄送机关"]),
    ("版记.印发", ["印发机关"]),
    ("页码.单右双左一字线7mm", ["单页居右空一字", "双页居左空一字", "一字线", "7mm"]),
    ("装订.左侧70mm", ["左侧装订", "70mm"]),
    ("避坑.发文机关标志形式", ["局文件"]),
    ("避坑.年份六角括号", ["〔〕"]),
    ("避坑.顺序号不编虚位", ["第01号", "1号"]),
    ("避坑.标题排列梯形菱形", ["长方形、沙漏形", "梯形、菱形"]),
    ("避坑.结构层次混用", ["混用"]),
    ("避坑.附件名称无标点", ["附件名称后不加标点"]),
